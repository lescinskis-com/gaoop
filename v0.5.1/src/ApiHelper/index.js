module.exports = (function() {
	var metricDimensionNames = {};
	var metricDimensionData = {};

	var helper = {
		generateApiName: function(name) {
			return `ga:${name}`;
		},
		sortMetricsDimensions: function(values) {
			var that = this;

			var result = {
				metrics: [],
				dimensions: []
			};

			values.forEach(function(value) {
				value = that.fixName(value);
				result[metricDimensionData[value]].push(value);
			});

			return result;
		},
		fixName: function(name) {
			name = name.toLowerCase();
			if (!(name in metricDimensionNames)) {
				return name;
			}
			return metricDimensionNames[name];
		},
		register: function(id, name, type) {
			metricDimensionNames[id] = name;
			metricDimensionData[name] = type;
		},
		registerMetric: function(name) {
			this.register(name.toLowerCase(), name, "metrics");
		},
		registerDimension: function(name) {
			this.register(name.toLowerCase(), name, "dimensions");
		}
	};

	for (metric of metrics) {
		helper.registerMetric(metric);
	}

	for (dimension of dimensions) {
		helper.registerDimension(dimension);
	}

	return helper;
})();
